<div class="wrap wrap2P">
<p class="marginLeft3 indent">Benjamin Righetti is also professor in charge of an organ class at the Conservatory and at Lausanne's University of Music (HEMU). As he had the privilege to study with exceptional musicians during his own musical education (Yves Rechsteiner, Jean-François Antonioli, François Delor, Jean Boyer, Jan Willem Jansen, Michel Bouvard, Philippe Lefèbvre...), it seems essential to him to pass on his knowledge and passion to his students now.</p>
<p class="spe">If you're interested in an organ education or if you have any questions about it, don't hesitate to contact Benjamin Righetti. You can also inquire directly at the Conservatory or at Lausanne's University of Music (HEMU). And for the occasional masterclasses, check the calendar section of this site, which references both concerts and these activities!</p>
